# biblioteca padrão

import platform

# Retorna o sistema
print(platform.system())

# Retorna o tipo de máquina
print(platform.machine())

# Retorna o (verdadeiro) nome do processador
print(platform.processor())

# Retorna a liberação do sistema
print(platform.release())

# Contém informações sobre a arquitetura de bits
# e o formato de ligação usado para o executável.
print(platform.architecture())
